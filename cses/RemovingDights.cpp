int dp(int x, VI &ans){
  if (ans[x] != -1){
    return ans[x];
  }
  int min_v = -1;
  int y = x;
  while (y){
    int r = y % 10;
    if (r) {
      if (min_v == -1 || min_v > dp(x - r, ans) + 1){
        min_v = dp(x - r, ans) + 1;
      }
    }
    y /=10;
  }
  return ans[x] = min_v;
}

